//Comparision operator

// Equal ==
3 == var1;
"3" == var1;
3 == '3';

// Not equal !=

var1 != '4';
var2 != 3;

//Strictly equal
3 === var1;

//Strict not equal
3 !== var2;

//Greater than
var2 > var1;
"12" > 2

//Greater than or equal
var2 >= var1;
"12" >= 2

//Less than
var2 < var1;
var2 < 2

//Less than or equal
var2 <= var1;
"12" <= 2

