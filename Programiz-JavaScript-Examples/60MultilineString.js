// JavaScript Program to Create Multiline Strings

const message = 'This is a long message\n' + 
    'that spans across multiple lines\n' + 
    'in the code.'

console.log(message);

// program to create a multiline strings using the \ operator
const message2 = 'This is a long message\n \
that spans across multiple lines\n \
in the code.'

console.log(message2);

// program to create a multiline strings using the template literal

const message3 = `This is a long message
that spans across multiple lines
in the code.`

console.log(message3);