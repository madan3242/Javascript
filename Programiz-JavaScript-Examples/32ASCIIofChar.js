// JavaScript Program to Find ASCII Value of Character

// ASCII Value of Character Using charCodeAt()

var string = 'b';

var result = string.charCodeAt(0);

console.log(`The ASCII vale is : ${result}`);

//ASCII Value of Character Using codePointAt()
string = 'z';

var result = string.codePointAt(0);

console.log(`The ASCII vale is : ${result}`);
