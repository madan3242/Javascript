//Using for loop
console.log("Using for loop");
let num = 10;
for (let i = 1; i <= num; i++) {
    console.log(i);  
}

//Using while loop
console.log("Using while loop");
var i = 1;
while (i <= num) {
    console.log(i);
    i++;
}

//Using do while
console.log("Using do while loop");
var i = 1;
do {
    console.log(i);
    i++;
} while (i <= num);